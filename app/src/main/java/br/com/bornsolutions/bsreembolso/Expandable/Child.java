package br.com.bornsolutions.bsreembolso.Expandable;

public class Child {

	private String Data;
	private String Valor;
	private int Image;

	public String getData() {
		return Data;
	}

	public void setData(String Name) {
		this.Data = Name;
	}

	public String getValor() {
		return Valor;
	}

	public void setValor(String Name) {
		this.Valor = Name;
	}

	public int getImage() {
		return Image;
	}

	public void setImage(int Image) {
		this.Image = Image;
	}
}
